export function getTranslation(language, key, fallback = '') {
  const parts = String(key || '').split('.').filter(Boolean);
  const read = (dict) => parts.reduce((value, part) => (value && value[part] !== undefined ? value[part] : undefined), dict);
  return read(translations[language]) ?? read(translations[DEFAULT_LANGUAGE]) ?? fallback ?? key;
}

